// need to have this class for liner type system

public class HuffData {

   public int x;
   public int y;
   public int w;
   public int v;
   public BitReserve br;

  public HuffData(int x, int y, int w, int v, BitReserve br) {
    this.x = x;
    this.y = y;
    this.w = w;
    this.v = v;
    this.br = br;
  }

}
